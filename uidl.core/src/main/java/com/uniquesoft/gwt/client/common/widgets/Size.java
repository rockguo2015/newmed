package com.uniquesoft.gwt.client.common.widgets;

public class Size {
	private int height;
	private int width;

	public Size( int width,int height) {
		this.height = height;
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public int getWidth() {
		return width;
	}

}
